    private void osveziBojeOcena() {
        JButton[] dugmici = {jButtonOcena1, jButtonOcena2, jButtonOcena3, jButtonOcena4, jButtonOcena5};
        for (int i = 0; i < dugmici.length; i++) {
            if (i + 1 == selektovanaOcena) {
                dugmici[i].setBackground(Color.ORANGE);
            } else {
                dugmici[i].setBackground(null);
            }
        }
    }
    
    private void inicijalizujLabele() {
        Pivo pivo = KorisnikService.getInstance().getSelektovanoPivo();
        Korisnik korisnik = KorisnikService.getInstance().getUlogovaniKorisnik();
        jLabelNazivPiva.setText(pivo.getNaziv());
        jLabelImeKorisnika.setText(korisnik.getIme() + " " + korisnik.getPrezime());
    }

