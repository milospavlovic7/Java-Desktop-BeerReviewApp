    private void jButtonDodajRecenzijuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajRecenzijuActionPerformed
        String tekst = jTextAreaRecenzijaPiva.getText().trim();
        
        //Validacija unosa
        if (tekst.isEmpty() || selektovanaOcena == 0) {
            JOptionPane.showMessageDialog(this, "Unesite ocenu i tekst recenzije.");
            return;
        }

        //Nova recenzija
        Recenzija nova = new Recenzija();
        nova.setKorisnikID(KorisnikService.getInstance().getUlogovaniKorisnik().getKorisnikID());
        nova.setPivoID(KorisnikService.getInstance().getSelektovanoPivo().getPivoID());
        nova.setSadrzaj(tekst);
        nova.setOcena(selektovanaOcena);
        nova.setDatumKreiranja(new Date());

        try {
            KorisnikService.getInstance().addRecenzija(nova);
            JOptionPane.showMessageDialog(this, "Recenzija uspešno dodata.");
            KorisnikService.getInstance().getPanelRecenzije().prikaziDetaljePiva(KorisnikService.getInstance().getSelektovanoPivo());
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greška prilikom dodavanja.");
        }
    }//GEN-LAST:event_jButtonDodajRecenzijuActionPerformed

