    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int res = JOptionPane.showConfirmDialog(this,
                "Da li ste sigurni da želite da obrišete nalog?", "Potvrda brisanja",
                JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            try {
                PivaraService.getInstance().deletePivara();
                SwingUtilities.getWindowAncestor(this).dispose();
                // new forms.IzborTipaKorisnika().setVisible(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Greška pri brisanju: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

