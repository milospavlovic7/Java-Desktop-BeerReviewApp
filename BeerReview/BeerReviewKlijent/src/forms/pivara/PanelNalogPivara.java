    private void loadUserData() {
        try {
            ulogovana = PivaraService.getInstance().getUlogovanaPivara();
            if (ulogovana != null) {
                txtNaziv.setText(ulogovana.getNaziv());
                txtEmail.setText(ulogovana.getEmail());
                txtTelefon.setText(ulogovana.getTelefon());
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greška pri učitavanju pivare: " + ex.getMessage());
        }
    }
    
