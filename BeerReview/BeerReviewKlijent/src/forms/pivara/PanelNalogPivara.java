    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String oldPass = new String(txtOldPassword.getPassword());
        String newPass = new String(txtPassword.getPassword());
        String confirm = new String(txtConfirmPassword.getPassword());
        if (oldPass.isEmpty() || newPass.length() < 2 || !newPass.equals(confirm)) {
            JOptionPane.showMessageDialog(this, "Unesite staru lozinku i novu lozinku (min 2 karaktera) dva puta.");
            return;
        }
        try {
            ulogovana = PivaraService.getInstance().updatePivara(
                    txtNaziv.getText(), txtTelefon.getText(), txtEmail.getText(), oldPass, newPass
            );
            PivaraService.getInstance().setUlogovanaPivara(ulogovana);
            JOptionPane.showMessageDialog(this, "Uspešno ažurirano.");
            loadUserData();
            setInitialState();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greška pri ažuriranju: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

